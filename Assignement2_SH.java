package BasicJavaProgram;

public class Assignement2_SH {

	public static void main(String[] args) 
	{
		//Declaring and initializing the variables for all data types
		
		byte a=127;
		System.out.println(a);
		//range from -128 to 127
		short b=32000;
		System.out.println(b);
		//range from -32768 to 32767
		int c=2147400000;
		System.out.println(c);
		//range from -2147483648 to -2147483647
		long d=9209808;
		System.out.println(d);
		//range from -9.223372e+18 to 9.223372e+17
		float e=42.545f;
		System.out.println(e);
		//range is 4 to 5 digits after decimal. Also make sure to add f for a float number
		double f=3.142434238432043204;
		System.out.println(f);
		//range is 19 digits after decimal
		char i='S';
		System.out.println(i);
		//range is alphabets
		boolean answer=true;
		System.out.println(answer);
		//range is either true or false
		String n="SheetalHary";
		System.out.println(n);
				

	}

}